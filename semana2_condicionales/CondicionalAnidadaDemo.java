public class CondicionalAnidadaDemo {
    public static void main(String[] args) {
        int numero = -5;
        if (numero > 0) {
            System.out.println("Positivo");
        } else if (numero < 0) {
            System.out.println("Negativo");
        } else {
            System.out.println("Cero");
        }
    }
}
